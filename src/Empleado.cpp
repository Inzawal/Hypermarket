void Empleado::Cargar(int id, Fecha fecha) {
    Menu validacion;
    bool cargoCorrecto = false;

    cout << "NOMBRE DEL EMPLEADO: ";
    scanf("%s",_nombre);                            // Es una forma de tomar todo el char, por mas que sea de mas de un solo caracter
    cout << endl;
    cout << "APELLIDO DEL EMPLEADO: ";
    scanf("%s",_apellido);
    cout << endl;
    cout << "SUELDO: $";
    _sueldo = validacion.validarInt(0,0,"SUELDO: $");
    cout << endl;

    while(cargoCorrecto == false) {
        cout << "CARGO DEL EMPLEADO: ";
        _cargo = validacion.validarChar();
        switch(_cargo) {

        case '1':
        case '2':
        case '3':
        case '4':
            cargoCorrecto = true;
            break;

        default:
            cout << "Ese cargo no es valido." << endl;
            break;
        }
        cout << endl;
    }
    _estadoLaboral = true;
    _dia = fecha.getDia();
    _mes = fecha.getMes();
    _anio = fecha.getAnio();
    _id = id;
}
