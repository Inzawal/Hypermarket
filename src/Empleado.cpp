void Empleado::Mostrar() {
    cout << "NOMBRE DEL EMPLEADO: " << getNombre() << endl;
    cout << "APELLIDO DEL EMPLEADO: " << getApellido() << endl;
    cout << "ID: " << getId()<< endl;
    cout << "SUELDO: $" << getSueldo()<< endl;
    cout << "CARGO DEL EMPLEADO: ";
    switch (getCargo()) {
    case '5':
        cout << "JEFE";
        break;
    case '4':
        cout << "GERENTE";
        break;
    case '3':
        cout << "CAJERO";
        break;
    case '2':
        cout << "REPOSITOR";
        break;
    case '1':
        cout << "LIMPIEZA";
        break;
    }
    cout << endl;
    cout << "ESTADO LABORAL: ";
    if(_estadoLaboral == true) {
        cout << "CONTRATADO" << endl;
    } else {
        cout << "DESPEDIDO" << endl;
    }
    cout << "FECHA DE CONTRATACION: " << _dia << "/" << _mes << "/" << _anio << endl;
    cout<<"---------------------------------"<<endl;
}
